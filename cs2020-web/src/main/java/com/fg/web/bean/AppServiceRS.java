package com.fg.web.bean;

public class AppServiceRS {
	
	private String token;
	private String domain;
	private String ip;
	private String androidVersion;
	private String androidContent;
	private String androidUrl;
	private String webCacheVersion;
	private String iosVersion;
	private String url;

	public String getToken() {
		return token;
	}
	public void setToken(String token) {
		this.token = token;
	}
	public String getDomain() {
		return domain;
	}
	public void setDomain(String domain) {
		this.domain = domain;
	}
	public String getIp() {
		return ip;
	}
	public void setIp(String ip) {
		this.ip = ip;
	}
	public String getAndroidVersion() {
		return androidVersion;
	}
	public void setAndroidVersion(String androidVersion) {
		this.androidVersion = androidVersion;
	}
	public String getAndroidContent() {
		return androidContent;
	}
	public void setAndroidContent(String androidContent) {
		this.androidContent = androidContent;
	}
	public String getAndroidUrl() {
		return androidUrl;
	}
	public void setAndroidUrl(String androidUrl) {
		this.androidUrl = androidUrl;
	}
	public String getWebCacheVersion() {
		return webCacheVersion;
	}
	public void setWebCacheVersion(String webCacheVersion) {
		this.webCacheVersion = webCacheVersion;
	}
	public String getIosVersion() {
		return iosVersion;
	}
	public void setIosVersion(String iosVersion) {
		this.iosVersion = iosVersion;
	}
	public String getUrl() {
		return url;
	}
	public void setUrl(String url) {
		this.url = url;
	}
	
	

}
